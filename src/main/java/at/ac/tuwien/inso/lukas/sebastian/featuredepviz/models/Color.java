package at.ac.tuwien.inso.lukas.sebastian.featuredepviz.models;

public class Color {

    int r;
    int g;
    int b;
    double a;

    public Color() {
    }

    public int getR() {
        return r;
    }

    public Color setR(int r) {
        this.r = r;
        return this;
    }

    public int getG() {
        return g;
    }

    public Color setG(int g) {
        this.g = g;
        return this;
    }

    public int getB() {
        return b;
    }

    public Color setB(int b) {
        this.b = b;
        return this;
    }

    public double getA() {
        return a;
    }

    public Color setA(double a) {
        this.a = a;
        return this;
    }
}
